package cn.itcast.industry.pojo;


public class Student {

  private long stuId;
  private String stuGender;
  private String stuName;
  private long stuMajid;
  private long stuDeptid;
  private long stuCourseid;


  public long getStuId() {
    return stuId;
  }

  public void setStuId(long stuId) {
    this.stuId = stuId;
  }


  public String getStuGender() {
    return stuGender;
  }

  public void setStuGender(String stuGender) {
    this.stuGender = stuGender;
  }


  public String getStuName() {
    return stuName;
  }

  public void setStuName(String stuName) {
    this.stuName = stuName;
  }


  public long getStuMajid() {
    return stuMajid;
  }

  public void setStuMajid(long stuMajid) {
    this.stuMajid = stuMajid;
  }


  public long getStuDeptid() {
    return stuDeptid;
  }

  public void setStuDeptid(long stuDeptid) {
    this.stuDeptid = stuDeptid;
  }


  public long getStuCourseid() {
    return stuCourseid;
  }

  public void setStuCourseid(long stuCourseid) {
    this.stuCourseid = stuCourseid;
  }

}
