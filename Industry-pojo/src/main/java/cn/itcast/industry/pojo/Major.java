package cn.itcast.industry.pojo;


public class Major {

  private long majId;
  private String majName;
  private long majDeptid;


  public long getMajId() {
    return majId;
  }

  public void setMajId(long majId) {
    this.majId = majId;
  }


  public String getMajName() {
    return majName;
  }

  public void setMajName(String majName) {
    this.majName = majName;
  }


  public long getMajDeptid() {
    return majDeptid;
  }

  public void setMajDeptid(long majDeptid) {
    this.majDeptid = majDeptid;
  }

}
